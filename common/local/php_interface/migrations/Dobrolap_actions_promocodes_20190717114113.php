        $sort = 900;
        /** @var DataManager $personalCouponManager */
        $personalCouponManager = App::getInstance()->getContainer()->get('bx.hlblock.personalcoupon');
        foreach ($this->actions as &$action) {
            /** Акции */
            $coupons = $action['COUPONS'];
            unset($action['COUPONS']);
            $action['DATE_ACTIVE_TO'] = '30.09.2019';
            $action['PROPERTY_VALUES']['ACTIVE_TO'] = '30.09.2019';
            $action['XML_ID'] = rand(100000, 199999);
            $action['SORT'] = $sort += 100;
            $actionID = $helper->Iblock()->addElement($iblockId, $action);

            /** Новое поле - тип купона - список - Обычный/Добролап */
            $hlblockId = $helper->Hlblock()->getHlblockId(HlblockCode::PERSONAL_COUPON);
            $entityId = 'HLBLOCK_' . $hlblockId;

            $fieldsID = $helper->UserTypeEntity()->addUserTypeEntityIfNotExists($entityId, 'UF_COUPON_TYPE', $this->couponTypeData);

            /** Заполняем новое польз свойство значениями */
            $userFieldEnum = new CUserFieldEnum();
            $userFieldEnum->SetEnumValues($fieldsID, $this->enums);
            $userFieldEnumDb = $userFieldEnum->GetList(
                [
                    'ID' => 'ASC'
                ],
                [
                    'USER_FIELD_ID' => $fieldsID
                ]
            );

            /** Получаем айди значения добролап */
            $dobrolapEnumID = null;
            while ($enum = $userFieldEnumDb->Fetch()) {
                if ($enum['XML_ID'] == 'dobrolap') {
                    $dobrolapEnumID = $enum['ID'];
                    break;
                }
            }

            /** Заполняем Промокоды */
            foreach ($coupons as $coupon) {
                $personalCouponManager::add([
                    'UF_PROMO_CODE'   => $coupon,
                    'UF_OFFER'        => $actionID,
                    'UF_DATE_CREATED' => new DateTime(),
                    'UF_DATE_CHANGED' => new DateTime(),
                    'UF_COUPON_TYPE'  => $dobrolapEnumID
                ])->getId();
            }
        }
        return true;
